def _apply_card_def_overrides(overrides: dict[str, dict[str, object]]) -> None:
    for card_id, changes in overrides.items():
        for defs in _CARD_DEF_GROUPS:
            if card_id in defs:
                defs[card_id] = replace(defs[card_id], **changes)
                break
        else:
            raise KeyError(f"unknown card def override target: {card_id}")


