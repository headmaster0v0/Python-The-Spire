    id: str
    rarity: CardRarity
    card_type: CardType = CardType.ATTACK
    base_damage: int = 0
    base_block: int = 0
    base_magic_number: int = 0
    cost: int = -1
    is_exhaust: bool = False
    is_ethereal: bool = False
    is_innate: bool = False
    is_retain: bool = False
    target_required: Optional[bool] = None
    is_unplayable: bool = False
    curse_effect_type: CurseEffectType = CurseEffectType.NONE
    curse_effect_value: int = 0
    upgrade_damage: int = 0
    upgrade_block: int = 0
    upgrade_magic_number: int = 0
    upgrade_cost: Optional[int] = None
    upgrade_exhaust: Optional[bool] = None
    upgrade_ethereal: Optional[bool] = None
    upgrade_innate: Optional[bool] = None
    upgrade_retain: Optional[bool] = None
    upgrade_description: Optional[str] = None
